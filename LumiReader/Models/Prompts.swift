        * 然后，用一两句话概括**主要内容**和**核心观点**，这是整个播报的“内容提要”。

    2.  **关键细节陈述**：
        * 用“首先”、“其次”、“另外”或“第一点”、“第二点”等清晰的顺序词，来逐条讲述文章的**关键细节**。
