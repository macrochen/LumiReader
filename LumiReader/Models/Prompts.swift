        * **忠于原文**：所有解读都必须来自文章本身。

    5.  **特殊情况处理**：
        * **软文识别**：如果判断是软文，请用清晰的口播方式提醒听众。例如：“请注意，接下来这篇文章《文章标题》，可能是一条推广信息，价值不大，我们直接跳过。”
